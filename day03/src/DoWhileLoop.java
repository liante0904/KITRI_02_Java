public class DoWhileLoop {
  public static void main(String[] args) {
    for(int i=10; i<10; i++){
      System.out.println(i);
      }

      int i = 10;
      while(i <10 ){
        System.out.println(i);
        i++;
      }
        System.out.println("before do while: i=" + i);
        do {
          System.out.println(i);
          i++;
        } while (i < 10);
        System.out.println("after do while: i=" + i);

      }
    }
